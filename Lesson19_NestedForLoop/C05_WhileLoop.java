package Lesson19_NestedForLoop;

public class C05_WhileLoop {
    public static void main(String[] args) {


        // 20 ile 50 arasi cift sayilar


        int bas = 20;
        int bit = 50;


        while (bas<=bit){
            if (bas%2==0){
                System.out.println(bas +" ");
            }
            bas++;              //formulu unutma
        }





    }
}
