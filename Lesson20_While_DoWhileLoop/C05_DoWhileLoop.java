package Lesson20_While_DoWhileLoop;

public class C05_DoWhileLoop {
    public static void main(String[] args) {

        int input = 14;

        do {
            System.out.println("Sayi ondan kucuktur : "+input);
            input++;
        } while (input<10);


        // input ondan kucuk DEGILDIR once yazdirir sonra kontrol eder

    }
}
