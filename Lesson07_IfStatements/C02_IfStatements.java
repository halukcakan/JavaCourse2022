package Lesson07_IfStatements;

public class C02_IfStatements {
    public static void main(String[] args) {


        int sayi=-10;

        if (sayi>0) {
            System.out.println("sayi pozitif");
        }
        if (sayi%2==0){
            System.out.println("sayi cift");
        }
        if (sayi%5==0){
            System.out.println("sayi besin kati");
        }


        /*
        sadece bir sarti kontrol etti ve devam etti diger secenekleri goz onune almadi
         */
    }
}
